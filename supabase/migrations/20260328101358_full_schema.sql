CREATE OR REPLACE FUNCTION "public"."create_quiz_with_questions"("input_story_id" "uuid", "input_user_id" "uuid", "input_total_tokens" integer, "input_questions" "jsonb") RETURNS "uuid"
    LANGUAGE "plpgsql"
    AS $$
declare
  new_quiz_id uuid := uuid_generate_v4();
begin
  -- Insert quiz with user_id and total_tokens
  insert into quizzes (id, story_id, user_id, total_tokens)
  values (new_quiz_id, input_story_id, input_user_id, input_total_tokens);

  -- Insert questions with user_id
  insert into quiz_questions (quiz_id, question, answer, user_id)
  select
    new_quiz_id,
    q->>'question',
    q->>'answer',
    input_user_id
  from jsonb_array_elements(input_questions) as q;

  return new_quiz_id;
end;
$$;


ALTER FUNCTION "public"."create_quiz_with_questions"("input_story_id" "uuid", "input_user_id" "uuid", "input_total_tokens" integer, "input_questions" "jsonb") OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."deduct_credit"("user_id" "uuid") RETURNS TABLE("success" boolean)
    LANGUAGE "plpgsql"
    AS $$
begin
  update users
  set story_credit = story_credit - 1
  where id = user_id
    and story_credit > 0
  returning true into success;

  if not found then
    return query select false;
  else
    return query select true;
  end if;
end;
$$;



CREATE OR REPLACE FUNCTION "public"."add_story_credits"("user_id_param" "uuid", "amount_param" integer) RETURNS "void"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
BEGIN
  UPDATE users
  SET story_credit = story_credit + amount_param
  WHERE id = user_id_param;
END;
$$;



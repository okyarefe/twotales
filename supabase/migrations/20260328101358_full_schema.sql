CREATE OR REPLACE FUNCTION "public"."handle_new_user"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'public', 'auth', 'pg_catalog'
    AS $$
BEGIN
  RAISE NOTICE 'Trigger fired for user: %', NEW.id;

  BEGIN
    INSERT INTO public.users (
      id,
      email,
      role,
      membership_type,
      story_credit,
      tts_credit
    )
    VALUES (
      NEW.id,
      NEW.email,
      COALESCE(NEW.raw_user_meta_data ->> 'role', 'user'),
      COALESCE(NEW.raw_user_meta_data ->> 'membership_type', 'basic'),
      COALESCE((NEW.raw_user_meta_data ->> 'story_credit')::INTEGER, 5),
      COALESCE((NEW.raw_user_meta_data ->> 'tts_credit')::INTEGER, 5)
    );

    RAISE NOTICE 'Successfully created user profile for: %', NEW.id;
  EXCEPTION
    WHEN OTHERS THEN
      RAISE WARNING 'Failed to create user profile for %: % (SQLSTATE: %)', 
        NEW.id, SQLERRM, SQLSTATE;
  END;

  RETURN NEW;
END;
$$;


ALTER FUNCTION "public"."handle_new_user"() OWNER TO "postgres";


